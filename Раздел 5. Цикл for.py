print("Задача 8. Даны два целых числа А и B (А < B). Найти произведение всех целых чисел от А до B включительно.")
print("Решение: ")
a = int(input("Введите a: "))
b = int(input("Введите b: "))
p = 1

if a < b :
    for i in range(a, b+1):
        p *= i  
elif b < a:
    for i in range(b, a+1):
        p *= i
else:
    p = str(a) + ", наверное"
print("Ответ: ", p)

